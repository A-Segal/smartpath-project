# האלגוריתם השני
# Grouped Delivery VRP with Time Constraints
# רקע כללי
# במערכת קיימת שכבת שיבוץ מוקדמת המתבצעת יום לפני יום החלוקה, שבה משויכים נזקקים למוקדי חלוקה. כל מוקד מכיל אחד או שני נזקקים, כאשר השיבוץ הקודם מבוצע כך שנשמרת קרבה גיאוגרפית יחסית בין הנזקקים המשויכים לאותו מוקד.
# ביום החלוקה עצמו נכנס שלב הביצוע בפועל, שבו מתנדבים מדווחים על מיקומם ההתחלתי, זמן הזמינות שלהם, וכן סוג הרכב שלהם.
# סוג הרכב נשמר במערכת כערך מספרי:
# -1  אופנוע
#  Mini-2
# Private - 3
#  Station-4
#  Mischari-5
# כאשר לכל סוג רכב מוגדרת קיבולת מקסימלית של משפחות/משלוחים שהמתנדב יכול לשאת בכל איסוף מהמוקד.
# הקיבולת אינה מייצגת את מספר המסירות הכולל לאורך היום, אלא את מספר המשפחות המקסימלי שהרכב יכול לשאת בכל רגע נתון.
# לדוגמה:
# • אופנוע - יכול לשאת משלוח אחד בכל איסוף
# - Mini • מספר קטן של משלוחים
# - Private • מספר בינוני של משלוחים
# - Station • מספר גדול יותר של משלוחים
# - Mischari • מספר גדול במיוחד של משלוחים
# הערכים המדויקים של הקיבולת נקבעים בטבלת המערכת וניתנים לשינוי בהתאם לצורכי הארגון.
# המסלול מורכב מרצף קבוצות משלוח, כאשר כל קבוצה מוגדרת כיחידה בלתי ניתנת לפירוק:
# מוקד → נזקק/ים
# המתנדב מחויב להגיע תחילה למוקד, לאסוף את כלל המנות של הקבוצה, ולהשלים את כל המסירות של אותה קבוצה לפני מעבר לקבוצה הבאה.
# לאחר השלמת המסירות של הקבוצה, הרכב מתפנה מקיבולת זו ויכול לאסוף משלוחים נוספים מקבוצה אחרת בהמשך המסלול.
# כלומר, הקיבולת נבדקת בכל שלב של איסוף משלוחים ואינה מצטברת לכל אורך היום.
# לאחר שיבוץ קבוצה למתנדב, המשלוח מסומן כשויך למתנדב כדי למנוע שיבוץ כפול.
# מטרת העל של האלגוריתם היא מקסום מספר המסירות במסגרת מגבלת הזמן ובכפוף למגבלת הקיבולת של המתנדב, ולאחר מכן מזעור זמן הנסיעה הכולל.
# מטרת האלגוריתם
# האלגוריתם נועד לבנות לכל מתנדב מסלול אופטימלי בהתאם ל:
# • זמן זמין
# • מיקום התחלתי
# • סוג רכב
# • קיבולת נגזרת מסוג הרכב
# • קבוצות משלוח זמינות (שאינן משויכות למתנדב)
# תוך מקסום מספר המסירות ועמידה בכל המגבלות.
#
# מודל חישובי (Graph Representation)
# הבעיה ממודלת כגרף מכוון משוקלל:
# • כל קבוצת משלוח היא צומת
# • המתנדב הוא נקודת התחלה
# • קשתות מייצגות זמן נסיעה בין מוקדים
# • לכל צומת זמן עיבוד פנימי קבוע (ביצוע חלוקה)
# בנוסף מוגדרת לכל מתנדב מגבלת קיבולת:
# - max_capacity לפי סוג הרכב
# ולכל קבוצת משלוח מוגדר:
# group_meals
#  group_families
# כאשר מספר המשפחות בקבוצה חייב להיות קטן או שווה לקיבולת הרכב בזמן האיסוף.
#
# מבנה מצב (State Representation)
# כל מצב בחיפוש כולל:
# • מיקום נוכחי של המתנדב
# • סוג הרכב
# • קיבולת הרכב
# • קבוצות שבוצעו
# • קבוצות שעדיין זמינות
# • זמן מצטבר
# • מספר מסירות שבוצעו
# • מסלול חלקי
#
# תהליך החיפוש
# האלגוריתם מבצע חיפוש מרחבי מצבים (State Space Search), שבו נבדקים כל הצירופים האפשריים של סדר קבוצות.
# בכל שלב נבחנות כל הקבוצות שעדיין לא בוצעו ושטרם שובצו למתנדב.
#
# הרחבת מצבים
# עבור כל מצב:
# נבדקות כל הקבוצות הזמינות, ובכל מעבר מחושבים:
# • זמן נסיעה למוקד הקבוצה
# • זמן ביצוע הקבוצה (מוקד → נזקקים)
# • מספר המשפחות בקבוצה
# • זמן כולל חדש
# מעבר מותר רק אם מתקיימים התנאים:
# • הזמן הכולל אינו חורג ממגבלת המתנדב
# • הקבוצה עדיין זמינה
# • מספר המשפחות בקבוצה אינו חורג מקיבולת הרכב
# כלומר:
# group_families ≤ max_capacity
# לאחר השלמת הקבוצה הקיבולת מתפנה, ולכן ניתן להמשיך לקבוצה נוספת כל עוד מגבלת הזמן מאפשרת זאת.
#
# יצירת מצב חדש ועדכון זמינות
# כאשר מעבר מתקבל:
# נוצר מצב חדש הכולל:
# • הוספת הקבוצה למסלול
# • עדכון מיקום לנקודת הסיום של הקבוצה
# • עדכון זמן מצטבר
# • עדכון מספר מסירות
# • סימון הקבוצה כבוצעה במסלול
# בנוסף מתבצע עדכון מערכת:
# • המשלוח מסומן כשויך למתנדב
# • המשלוח מוסר ממאגר המשלוחים הזמינים
#
# מניעת חזרות (Pruning)
# כדי למנוע חישוב כפול של מצבים:
# נשמר לכל מצב:
# • סט קבוצות שבוצעו
# • הזמן הטוב ביותר שהושג עבור מצב זה
# אם מתקבל מצב זהה עם זמן גרוע יותר → הוא נפסל.
#
# תנאי סיום החיפוש
# החיפוש מסתיים כאשר:
# • אין עוד מצבים להרחבה
# או
# • כל הקבוצות האפשריות נוצלו בהתאם למגבלות הזמן והקיבולת
#
# בחירת פתרון אופטימלי
# מתוך כל המסלולים החוקיים:
# 1.	נבחר המסלול עם מספר המסירות המקסימלי
# 2.	במקרה של שוויון → נבחר המסלול עם זמן נסיעה כולל מינימלי
#
# עדכון המערכת
# בסיום החיפוש:
# לכל DeliveryAssignment שנבחר:
# VolunteerID ← volunteer.id
# בטבלת:
# DeliveryAssignment
# כך שהאלגוריתם השני משלים את השיבוץ שבוצע באלגוריתם הראשון ומקצה בפועל מתנדב לכל משלוח.
#
# תוצאה סופית
# הפלט הסופי כולל:
