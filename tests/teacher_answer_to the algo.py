# תיאור האלגוריתם
# המערכת מקבלת:
# •	רשימת מוקדי חלוקה, כאשר לכל מוקד יש:
# o	מזהה (id)
# o	כמות מנות זמינה
# •	רשימת נזקקים, כאשר לכל נזקק יש:
# o	מזהה (id)
# o	כמות מנות נדרשת
# בנוסף קיימים נתוני המיקום של כל מוקד ונזקק, המאפשרים לחשב את המרחק ביניהם.
# שלב 1 – בניית אפשרויות השיבוץ
# עבור כל מוקד נבנה מילון רשימות של כל הנזקקים שניתן לשבץ אליו.
# <<(ID, Score נזקק ) List  ,מוקד ID >> Dictionary
#
# נזקק נחשב מועמד רלוונטי למוקד אם מתקיימים שני התנאים:
# 1.	המרחק בין המוקד לנזקק אינו עולה על 100 ק"מ.
# 2.	כמות המנות הנדרשת על ידי הנזקק קטנה או שווה לכמות המנות הזמינה במוקד.
# לכל זוג אפשרי של מוקד–נזקק מחושב ערך התאמה (Score) לפי הנוסחה:
# Score = (distance * 0.6) + (remaining_meals_ratio * 0.4)
# כאשר remaining_meals_ratio = מנות המוקד לאחר השיבוץ / כמות המנות המקורית של המוקד
# לאחר חישוב ה־Score, נשמרת עבור כל מוקד רשימה של כל הנזקקים האפשריים יחד עם ציון ההתאמה שלהם.
# הרשימה ממוינת בסדר עולה (Timsort) לפי ה־Score, כך שהמועמד הראשון ברשימה הוא המועמד האופטימלי ביותר עבור אותו מוקד.-הציון יוצא נמוך והוא הטוב
# לדוג' נזקק במרחק 80 ק"מ רוצה 5 מנות 5*0.4+80*0.6= 48.1
# נזקק במרחק 40 ק"מ רוצה 19 מנות  19*0.4+40*0.6=24.38
# שלב 2 – יצירת תור עבודה
# נוצר תור המכיל את כל המוקדים.   <מוקדID> Queue
# בנוסף נשמר מילון של השיבוצים בפועל, שבו עבור כל נזקק נשמר:
# •	המוקד שמשובץ אליו כעת.
# •	ציון ההתאמה (Score) של השיבוץ הנוכחי. (אוביקט של מוקדID וscore  ,value: נזקק key :ID)
# בתחילת התהליך מבנה זה ריק.
# שלב 3 – תהליך השיבוץ
# כל עוד קיימים מוקדים בתור:
# 1.	מוציאים את המוקד הראשון מהתור.
# 2.	בוחרים את המועמד הנוכחי שלו מתוך רשימת המועמדים הממוינת.
# 3.	בודקים את מצב הנזקק במבנה השיבוצים.
# מקרה א' – הנזקק אינו משובץ
# אם הנזקק עדיין אינו משובץ:
# •	מבצעים שיבוץ.
# •	שומרים עבורו את מזהה המוקד ואת ה־Score.
# המוקד סיים את תפקידו ואינו חוזר לתור-מקטינים את כמות המנות שהוא יכול לתת -אם יש לו עוד מנות לתת הוא יישמר לסיבוב נוסף-בו יחושב הציון על כל הנזקקים שלא שובצו לפי ערך ה score בהתאם לכמות מנות שנשארו לחלק
# מקרה ב' – הנזקק כבר משובץ
# אם הנזקק כבר משויך למוקד אחר:
# משווים בין שני ציוני ההתאמה.
# אם ה־Score  החדש טוב יותר - new_score < current_score
# אז:
# 1.	מחליפים את השיבוץ לנזקק.
# 2.	המוקד החדש הופך לבעל השיבוץ.
# 3.	המוקד הקודם מאבד את הנזקק.
# 4.	המוקד הקודם מוחזר לסוף התור.
# 5.	המוקד הקודם מתקדם למועמד הבא ברשימתו.
# אם ה־Score  הקיים טוב יותר - current_score <= new_score
# אז:
# 1.	השיבוץ הקיים נשאר ללא שינוי.
# 2.	המוקד הנוכחי מתקדם למועמד הבא ברשימתו.
# 3.	המוקד הנוכחי מוחזר לסוף התור כדי לנסות שיבוץ אחר.
# התקדמות ברשימת המועמדים
# לכל מוקד נשמר מצביע למיקום הנוכחי ברשימת המועמדים שלו.
# כאשר מועמד מסוים נפסל או נתפס על ידי מוקד מתאים יותר:
# לא מוחקים אותו מהרשימה.
# במקום זאת המוקד פשוט מתקדם למועמד הבא.
# גישה זו מונעת מחיקות והעתקות מיותרות ומשפרת את ביצועי האלגוריתם.
# תנאי סיום
# האלגוריתם מסתיים כאשר תור המוקדים מתרוקן או שכל הנזקקים שובצו
# בשלב זה:
# •	כל מוקד שובץ לנזקק המתאים ביותר שהיה זמין עבורו לאורך התהליך.
# •	לכל נזקק נשמר השיבוץ בעל ה־Score הטוב ביותר שנמצא עבורו.
# •	אין יותר מוקדים שצריכים לנסות מועמדים נוספים.
# רעיון מרכזי
# האלגוריתם פועל בשיטת תחרות על נזקקים:
# •	כל מוקד מנסה לקבל את המועמד הטוב ביותר עבורו.
# •	כאשר מספר מוקדים מעוניינים באותו נזקק, הנזקק נשאר אצל המוקד בעל ה־Score  הטוב ביותר.
# •	המוקד שהפסיד ממשיך לנסות את המועמד הבא ברשימתו.
# •	התהליך נמשך עד שלא נותרו מוקדים שצריכים לבצע ניסיון שיבוץ נוסף או שכל הנזקקים שובצו.
#
# מבני הנתונים:
# רכיב	מבנה
# מאגר מוקדים	Dictionary של
# מועמדים של מוקד	List ממוין
# התקדמות ברשימה	CurrentIndex
# שיבוצי נזקקים	Dictionary
# ניהול עבודה	Queue
#
# סיבוכיות:
#   C – מספר המוקדים (Centers) במערכת.
#   R – מספר הנזקקים (Recipients) במערכת.
#   E – מספר ההתאמות האפשריות שעומדים בתנאי הסף ונשמרו כרלוונטיים לאחר הסינון
# רכיב	זמן
# יצירת כל ההתאמות	O (C *N)
# מיון מועמדים	O (E log N)
# שלב השיבוץ	O(E)
# חיפוש בטבלת שיבוצים	O (1)
# עדכון בטבלת שיבוצים	O (1)
# שליפה מהתור	O (1)
# הכנסה לתור	O (1)
# מעבר למועמד הבא	O (1)
#
# זו שאלה מעולה שנוגעת באחד האתגרים המורכבים ביותר בגרסאות מתקדמות של אלגוריתם גייל-שפלי. כאשר קיבולת המוקד (כמות המנות) משתנה תוך כדי ריצה, הדירוג הראשוני שביצענו מאבד מהדיוק שלו, מכיוון שהציון מבוסס בין היתר על יחס המנות.
# כדי לטפל במצב שבו נזקק לא לקח את כל המנות, קיימות שתי גישות מרכזיות שכדאי לשקול. הגישה האופטימלית תלויה בשאלה עד כמה קריטי לך לשמור על דיוק מתמטי מוחלט לעומת חסכון בזמן ריצה.
# אפשרויות לטיפול במוקד עם יתרת מנות
# 1. שיטת הסיבובים (Rounds) - הגישה המומלצת
# זוהי הגישה המדויקת ביותר, והיא למעשה תואמת את הרעיון שכבר תוכנן במסמך שלך.
# •	המוקד שמצא שיבוץ אך נשאר עם עודף מנות מסוים, לא יוחזר באופן מיידי לאותו התור.
# •	במקום זאת, מקטינים את כמות המנות שהמוקד יכול לתת, והוא נשמר בצד לסיבוב נוסף.
# •	בסיבוב החדש, יחושב שוב ציון ההתאמה עבור כל הנזקקים שנותרו ללא שיבוץ, בהתאם לכמות המנות המעודכנת שנשארה לחלוקה.
# •	היתרון: הגישה מבטיחה שהשיבוץ תמיד יתבסס על נתונים מעודכנים וציונים מדויקים. היא שומרת על יציבות האלגוריתם.
# 2. המשכיות רציפה עם "סינון עצל" (Lazy Filtering)
# אם המטרה היא להימנע מריצה של סיבובים נוספים ולסיים הכל בתור עבודה אחד, ניתן להחזיר את המוקד לתור הקיים, אך עם התאמות.
# •	המוקד ממשיך להשתמש באותו מצביע השומר את מיקומו הנוכחי ברשימת המועמדים שלו.
# •	כאשר שולפים את המוקד מהתור לבחינת המועמד הבא, יש לבדוק האם כמות המנות שהנזקק דורש קטנה או שווה לכמות המנות הזמינה העדכנית במוקד.
# •	אם הנזקק דורש יותר ממה שנשאר, פשוט מדלגים עליו ועוברים למועמד הבא ברשימה.
# •	החיסרון: מכיוון שנוסחת הציון מבוססת בחלקה על כמות המנות, הרשימה הממוינת שנוצרה בשלב 1 כבר אינה משקפת את הסדר האופטימלי המדויק עבור יתרת המנות החדשה.
